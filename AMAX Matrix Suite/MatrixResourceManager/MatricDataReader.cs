using System;
using GLib;
using System.Collections.Generic;

namespace MatrixResourceManager
{
	public class MatricDataReader
	{
		int metricId;
		int timeFrame;
		List<String> gpuUUIDList = new List<string>();
		String gpuServerIpId;

		public MatricDataReader (String gpuServerIpId, List<String> gpuUUIDList, int metricId, int timeframe)
		{
			this.gpuServerIpId = gpuServerIpId;
			this.gpuUUIDList = gpuUUIDList;
			this.timeFrame = timeframe;
			this.metricId = metricId;
		}




	}
}

